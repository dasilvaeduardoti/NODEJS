module.exports = {
    soma,
    subtracao,
    divisao,
    multiplicacao
}

function soma(num1,num2){
    return num1+num2;
}
function subtracao(num1,num2){
    return num1-num2;
}   
function divisao(num1,num2){
    return num1/num2;
}   
function multiplicacao(num1,num2){
    return num1*num2;
}